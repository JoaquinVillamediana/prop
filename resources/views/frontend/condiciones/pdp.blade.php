</br>
2.2 W.B.O. Software no recopilará a través del Sitio Web ninguna información sobre usted que pueda identificarlo personalmente, como, por ejemplo, su nombre, dirección, número de teléfono o dirección de correo electrónico (en adelante, la “Información Personal"), a menos que usted nos la brinde en forma voluntaria. Si usted no desea que recopilemos su Información Personal, por favor absténgase de proporcionarla. Sin embargo, deberá tener en cuenta que en caso de no proporcionarnos su Información Personal, no podrá acceder a los servicios e información disponibles a través del Sitio Web.
